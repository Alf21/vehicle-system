package me.alf21.handling.planeData;


public class Gear {
	
	private double upR, downL;
	
	public Gear(double upR, double downL) {
		this.upR = upR;
		this.downL = downL;
	}
	
	/**
	 * @return GearUpR
	 */
	public double getUpR() {
		return upR;
	}

	/**
	 * @return GearDownL
	 */
	public double getDownL() {
		return downL;
	}
	
}
